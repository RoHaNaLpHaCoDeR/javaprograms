public class CWH_04_Literals {
    public static void main(String [] args){
        byte age= 34;
        System.out.println(age);
        char ch = 'A';
        int age2 = 56;
        long age4 = 4444444666666L;
        float f1 = 5.6f;
        double d1= 5.66d;
        System.out.println(ch+ age2 + age4 + f1+d1);
        boolean a = true;
        System.out.println(a);
        String r = "Rohan Arora";
        System.out.println(r);
    }
}
